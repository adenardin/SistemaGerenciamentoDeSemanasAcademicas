    public PalestranteBO readPalestrante(){
        
        String nomePalestrante = txtNome.getText();
        String codigo = txtCodPalestrante.getText();
            int codigoPalestrante = Integer.parseInt(codigo);
        String eMail = txtEmail.getText();
        String telefone = txtTelefone.getText();
        String strMiniCurriculo = txtMiniCurriculoPalestrante.getText();
        
        return new PalestranteBO(codigoPalestrante, nomePalestrante, strMiniCurriculo, eMail, telefone);
    }    

    public void findPalestrante(PalestranteBO c){
        txtNome.setText(c.getNomePalestrante());
        txtCodPalestrante.setText(String.valueOf(c.getCodPalestrante()));
        txtEmail.setText(c.geteMail());  
        txtTelefone.setText(c.getTelefone()); 
        txtMiniCurriculoPalestrante.setText(c.getMiniCurriculoPalestrante());
    }  
    
    public void retrievePalestrante(){
        DefaultTableModel modelinho = new DefaultTableModel();

        modelinho.addColumn("Codigo Palestrante");
        modelinho.addColumn("Nome");
        modelinho.addColumn("Mini Curriculo");
        modelinho.addColumn("E-mail");
        modelinho.addColumn("Telefone");

        tblPalestrante.setModel(modelinho);        

        PalestranteCL.showPalestrante().stream().forEach((c) -> {
            modelinho.addRow(new Object[]{
                c.getCodPalestrante(),
                c.getNomePalestrante(),
                c.getMiniCurriculoPalestrante(),
                c.geteMail(), 
                c.getTelefone()
            });
        });
        tblPalestrante.setModel(modelinho);
    }
    
