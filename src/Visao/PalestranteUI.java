    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        PalestranteBO cliente;
        try {
            cliente = readPalestrante();
            PalestranteCL.newPalestrante(cliente);
            JOptionPane.showMessageDialog(null, "Palestrante cadastrado com sucesso!");
            retrievePalestrante();
            txtCodPalestrante.setText("");
            txtNome.setText("");
            txtMiniCurriculoPalestrante.setText("");
            txtEmail.setText("");
            txtTelefone.setText("");
        } catch (Exception ex) {
        }        
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        int     cod = Integer.parseInt(txtCodPalestrante.getText());
//        double  rend = Double.parseDouble(txtRenda.getText());
        String  nom = txtNome.getText();
        
        if(nom != null){
            PalestranteBO c = PalestranteCL.findPalestranteNome(nom);
            findPalestrante(c);
        }else if(String.valueOf(cod) != null){
            
            String str = String.valueOf(cod);
            // suppose str becomes null after some operation(s).
            int number = 0;
            try
            {
                if(str != null)
                  number = Integer.parseInt(str);
            }
            catch (NumberFormatException e)
            {
                number = 0;
            }            
            
            PalestranteBO c = PalestranteCL.findPalestranteCod(number);
            findPalestrante(c);
        }else{
            JOptionPane.showMessageDialog(null,"Digite um parâmetro válido!");
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnPesquisarActionPerformed

