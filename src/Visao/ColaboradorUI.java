    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        int cod = Integer.parseInt(txtCodigoColaborador.getText());
//        double  rend = Double.parseDouble(txtRenda.getText());
        String nom = txtNomeColaborador.getText();

        if (nom != null) {
            ColaboradorBO c = ColaboradorCL.findColaboradorNome(nom);
            findColaborador(c);
        } else if (String.valueOf(cod) != null) {

            String str = String.valueOf(cod);
            // suppose str becomes null after some operation(s).
            int number = 0;
            try {
                if (str != null) {
                    number = Integer.parseInt(str);
                }
            } catch (NumberFormatException e) {
                number = 0;
            }

            ColaboradorBO c = ColaboradorCL.findColaboradorCod(number);
            findColaborador(c);
        } else {
            JOptionPane.showMessageDialog(null, "Digite um par√¢metro v√°lido!");
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

