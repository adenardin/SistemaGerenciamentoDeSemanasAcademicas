    public ColaboradorBO readColaborador() throws ParseException {
        
        int codigoColaborador = Integer.parseInt(txtCodigoColaborador.getText());
        String DataNascimento = txtDataNascimento.getText();
            SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");
            Date dataNascimentoColaborador = format.parse(DataNascimento);
        String eMailColaborador = txtEmail.getText();
        String telefoneColaborador = txtTelefone.getText();
        String nomeColaborador = txtNomeColaborador.getText();
        String repitaSenha = txtRepitaSenha.getText();
        String senhaColaborador = txtSenha.getText();
        Boolean statusColaborador = Boolean.parseBoolean(txtStatus.getText());
 
        return new ColaboradorBO(
            codigoColaborador,  
            nomeColaborador,  
            eMailColaborador, 
            telefoneColaborador, 
            dataNascimentoColaborador, 
            senhaColaborador, 
            statusColaborador
        );
    }
    
    public void findColaborador(ColaboradorBO c) {
        txtCodigoColaborador.setText(String.valueOf(c.getCodColaborador()));        
        SimpleDateFormat dn = new SimpleDateFormat("dd/MM/yyyy");
            txtDataNascimento.setText(String.valueOf(dn.format(c.getDataNascimencolaborador())));        
        txtEmail.setText(c.geteMailColaborador());
        txtNomeColaborador.setText(c.getNomeColaborador());
        txtSenha.setText(c.getSenhaColaborador());
        txtStatus.setText(String.valueOf(c.getStatusColaborador()));
        txtTelefone.setText(c.getTelefoneColaborador());
    }

    public void retrieveColaborador() throws ParseException {
        DefaultTableModel modelinho = new DefaultTableModel();
        modelinho.addColumn("C√≥digo");
        modelinho.addColumn("Nome");
        modelinho.addColumn("E-mail");
        modelinho.addColumn("Telefone");
        modelinho.addColumn("Data de Nascimento");
        modelinho.addColumn("Senha");
        modelinho.addColumn("Status");
        tblColaborador.setModel(modelinho);
        ColaboradorCL.showColaborador().stream().forEach((c) -> {
            // Define o formato da data e ap√≥s aplica mascara para preencher os campos de texto.
            Format formatter = new SimpleDateFormat("dd/MM/yyyy");
            String dataNascimento = formatter.format(c.getDataNascimencolaborador());
            modelinho.addRow(new Object[]{
                c.getCodColaborador(),
                c.getNomeColaborador(),
                c.geteMailColaborador(),
                c.getTelefoneColaborador(),
                dataNascimento,
                c.getSenhaColaborador(),
                c.getStatusColaborador()
            });
        });
        tblColaborador.setModel(modelinho);
    }

